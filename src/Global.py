def find_pos(data, id) -> int:
  pos: int = -1
  if 0 < id < len(data) - 1:
    if data[id].id == id:
      return id

  for i, d in enumerate(data):
    if id == d.id:
      pos = i
      break

  return pos
# -----------------------------------------------------------------------------
def get_el_nodes(connectivity: list[int], nodes):
  el_nodes = []
  for id in connectivity:
    pos = find_pos(nodes, id)
    if pos > -1:
      el_nodes.append(nodes[pos])
  return el_nodes
